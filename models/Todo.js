//---------------------------------------------подключаемые модули------------------------------------------------------------------------------------

const { Schema, model } = require('mongoose')
//---------------------------------------------подключаемые модули------------------------------------------------------------------------------------
//---------------------------------------------создание модели----------------------------------------------------------------------------------------

const schema = new Schema({
  title: {
    type: String,
    required: true
  },
  completed: {
    type: Boolean,
    default: false
  }
})
//---------------------------------------------создание модели----------------------------------------------------------------------------------------

module.exports = model('Todo', schema)
